            built over a century ago and still operating!
        </li>
        <li>
            - Admire the lovely <strong>St. Basil's Cathedral!</strong> The French diplomat Marquis de Custine
            commented that it combined "the scales of a golden fish, the enamelled skin of a serpent,
            the changeful hues of the lizard, the glossy rose and azure of the pigeon's neck" and
            wondered at "the men who go to worship God in this box of confectionery work."
        </li>
        <li>
            - Walk by <strong>Lobnoye Mesto</strong> (literally meaning
