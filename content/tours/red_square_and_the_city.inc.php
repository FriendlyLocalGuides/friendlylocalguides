    <h6>Kitai-gorod:</h6>
    <p class="intro-txt">Stroll along medieval <strong>Kitai-gorod</strong> with its strong ancient Russia feel,
        known for its bohemian lifestyle, markets and arts.</p>
    <ul>
        <li>
            - <strong>Nikolskaya Street.</strong> Here you will find Russia's first
            publishing house, the second oldest monastery, and Ferryn Pharmacy, known as the number one pharmacy back
            in Soviet times and famous for its Empire-style architecture.
        </li>
        <li>
            - <strong>Ilinka Street.</strong> The financial street of the Kitaigorod (China Town)
            district, where you’ll find the Gostiny Dvor (Merchant’s Yard), which is now a showroom for Ferraris and
            Maseratis. The street was designed in the 1790s by Catherine the Great.
        </li>
        <li>
            - <strong>Varvarka street.</strong> The oldest street in Moscow, which
            dates back to the 14th century, and still has remnants of early Muscovite architecture, such as the
            Old English Court and the Palace of the Romanov’s.
        </li>
        <li>
            - <strong>Kamergersky Lane.</strong> Only a small road of about 250
            meters, it is home to some of the oldest artifacts of the city, as each building holds a fascinating
            story. Some of Russia’s most famous writers, poets, and composers from as far back as the Golden Age of
            Russian culture, have lived or worked on this lane.
        </li>
    </ul>
    <h6>Historic City Center</h6>
    <p class="intro-txt">
        Walk the historical old center of Moscow with its cool local vibe, including the main
        <strong>Tverskaya street</strong>, and indulge in desserts in Moscow’s first grocery <strong>“Eliseev’s store”</strong>, housed in an 18th
        century neoclassical building, famous for its baroque interior and decoration.
    </p>
