<h5>Itinerary:</h5>
<ul>
    <li>
        <h6>Nevsky Prospect</h6>
        <p>We'll walk up Nevsky Prospect, the main street of Saint Petersburg, the oldest (almost 300 years old), one of the widest (up to 60 m) and one of the longest (4.5 km)!</p>
        <p>About 2 million people walk in Nevsky Prospect every day, and we'll be the happiest of these 2 millions, as the ultimate St Petersburg experience is waiting for us!</p>
    </li>
    <li>
        <h6>Beloselsky Beloozersky Palace</h6>
        <p>Let's have a look at the last private palace built on Nevsky Prospe?t in the XIX century, which was considered one of the most beautiful private residencies in St. Petersburg.</p>
        <p>Since 2003, the palace is owned by the Office of Presidential Affairs of the Russian Federation.</p>
        <p>Beloselsky-Belozersky Palace is included in the Unified State Register of cultural heritage of Russia.</p>
    </li>
    <li>
        <h6>Anichkov Bridge and Palace</h6>
        <p>One of our favorite spots for taking memorable photos, though every sight in our list is worth a million shots.</p>
        <p>Breathtaking bridge with very detailed sculptures of horses and extremely elegant iron work. Dramatic and impressive scene of Riders taming the Horses.</p>
        <p>There is an old joke here: ask locals if there are any non-drinkers in the city, and they will tell you there are four in St Pete - riders on Anichkov bridge, their hands busy as they are taming the horses.</p>
        <p>The story about the horses is very interesting. Under each horse you see the faces sculpted onto the underside of one of them. You'll take your chances and try to guess on the genitals of which horse the sculptor Klodt painted face his wife's lover.</p>
    </li>
    <li>
        <h6>Eliseev Emporium</h6>
        <p>One of the most striking examples of posh St. Petersburg Art Nouveau architecture. The legendary food shop has 7 departments of exquisite delicacies, with the largest display dedicated to mouth watering desserts.</p>
        <p>12 mobile etageres hold over 3,000 varieties of exclusive products. Luxury and unique atmosphere is in every detail of the shop: ornate vitrines, upholstered sofa, gilded stucco ceilings, gold leaf glasses, glitzy chandeliers, the hand-finished floral cornices and the original equipment used in the Imperial era!</p>
        <p>You might want to make a very quick exit from the shop, as you could easily spend a fortune here.</p>
        <p>However, it's just the right spot for a yummy cappuccino with a cake to die for. Live classical singing will relax your mind.</p>
    </li>
    <li>
        <h6>Kazan Cathedral</h6>
        <p>The Kazan cathedral will be an unexpected treasure for you on busy and dynamic Nevsky Prospect (yes, we're still there).</p>
        <p>The elegance of the interior of the cathedral is stunning! The architecture, sculpture and painting are united in a unique harmony.</p>
        <p>The Cathedral's 136 columns, a 80m-high dome, intricate structure and interior make it one of the city's most majest? cathedrals.</p>
        <p>The cathedral's huge bronze doors framed in marble are one of three copies of the original doors created by the sculptor Ghiberti for the baptismal house in Florence, Italy in the XV century (the other two are in San Francisco and on the Baptistry itself).</p>
        <p>September 1, 2011 the Bank of Russia has issued a commemorative coin of 25 rubles with the image of the Kazan Cathedral in St. Petersburg in the series "Architectural Monuments of Russia."</p>
    </li>
    <li>
        <h6>Gostiny Dvor</h6>
        <p>The historical symbol of St Petersburg and the city's oldest and largest shopping center can be compared to Moscow's Gum, but Gostiny Dvor being much older ? more than 100 years older. In fact, Gostiny Dvor is one of the first shopping arcades in the world, first mentioned in 1748.</p>
        <p>It's also one of the most fashionable shopping centres in Eastern Europe.</p>
    </li>
    <li>
        <h6>St Isaac's Cathedral</h6>
        <p>The second tallest Orthodox cathedral in the world after Christ the Savior cathedral in Moscow. It took 40 years, one million golden roubles, more than 100kg of gold leaf for the high dome alone, the work of 400 000 workers and literally the whole life of the French architect Auguste Ricard de Montferrand to construct St Isaac's cathedral.</p>
        <p>Impressive size, unique shape, obsessively decorated interior, vibrant interior colors and amazing mosaics may make you wondering if that isn't too much for the cathedral? But well, those were the times of Emperors, and Saint Petersburg was the capital of Imperial Russia.</p>
        <p>If you dare to climb 262 steps, you'll be rewarded with fantastic city views and a 360 degree panorama of St Pete. Shall we?</p>
    </li>
    <li>
        <h6>State Hermitage Museum</h6>
        <p>Now, this one is literally Massive. Hermitage collection contains about 3 million items housed in 350 halls of 5 buildings.</p>
        <p>To explore the entire collection, stopping at each exhibit only for 1 minute, you will have to spend 8 years of life and walk more than 20 kilometers.</p>
        <p>We'll not go inside the Hermitage on our relatively super short 8-hour tour. If you want to get inside, we advise you to do that early morning and be ready to spend the the whole day for exploration.</p>
    </li>
    <li>
        <h6>Palace Square, Palace Bridge and Palace embankment</h6>
        <p>Let's move on to Palace land. Palace Square easily surpasses Red Square, being at least twice as big.</p>
        <p>This striking square unites three other sights of the city, all of high significance and in TOP bucket list: Nevsky Prospect with Palace Bridge leading to Vasilievsky Island. As you understand, this is the heart of St Petersburg.</p>
        <p>This is where all cultural and historical giants are placed. This is the place for the events of worldwide significance, including the Bloody Sunday (1905), a fierce battle during the October Revolution of 1917, military performance in 1920 with 6 000 artists performing the scenes of the October Revolution of 1917, concerts by Elton John, Sting, Shakira and Madonna and huge performances in 2014 devoted to a 100 year anniversary of the end of WWI and a 250 year anniversary of Hermitage.</p>
        <p>After Winter Palace as built in 1762, the area was littered with debris. Emperor Peter III was able to get rid of it quickly and creatively - he allowed people to take anything they want from the square for free. In a few hours the whole area was cleared.</p>
        <p>Petersburg Palace Square is included in the UNESCO World Heritage List, and in all our St Petersburg tours.</p>
    </li>
    <li>
        <h6>Alexander Column</h6>
        <p>The column is a prototype of the ancient Trajan's Column in Rome. "Alexander Column". With the height of 47.5 meters and the total weight of 704 tons, the Alexander Column is? is not dug into the ground, neither it is attached to the foundation.</p>
        <p>Yes, it stands on a granite base without any additional support, under its own weight!</p>
        <p>Surprisingly, the setting of the column took only 1 hour 45 minutes. 2 thousand soldiers and workers were involved in the ambitious operation.</p>
        <p>At first, the residents feared to pass the column and stayed away from it. Let's see how you will feel in front of this wild giant.</p>
    </li>
    <li>
        <h6>General Staff Building</h6>
        <p>Tsar Peter I took an active part in construction of the General Stuff building in 1705. The working day used to start at 5am and finish at 9pm. Our 8-hour tour is exactly a half of the working day of those times.</p>
        <p>Admiralty is decorated with the 70 m high spire. On the edge of the spire there is a gilded ship weighing 65 kg. The weight of the gold used for gilding is 2 kg.</p>
        <p>The 580 m long façade consists of two separate buildings united with a triumphal arch. The arch is a symbol Russia's military triumph. it forms a symmetrical axe with the central part of the Winter Palace which is in front of it.</p>
    </li>
    <li>
        <h6>Winter Palace</h6>
        <p>Winter Palace has been painted in almost all colors of the rainbow. It was red and pink, and yellow. Finally, it turned into pale green in 1946, and is still wearing this mask.</p>
        <p>Now more colorful facts about the Palace:</p>
        <p>117 staircases</p>
        <p>1084 rooms</p>
        <p>1786 doors</p>
        <p>1945 windows</p>
        <p>5 million bricks used</p>
        <p>Winter Palace is part of the Hermitage and we keep to our earlier advice ? be ready to spend the whole day to explore The Museum.</p>
    </li>
    <li>
        <h6>Moika Embankment</h6>
        <p>Moika river is one of the longest n St Petersburg with the length of 4.67 km.</p>
        <p>The banks are covered with a ridiculously huge amount of historical sites. The superb views on both sides of the river make Moika embankment the most romantic place in St. Petersburg.</p>
        <p>Moika is included in the Unified State Register of cultural heritage of Russia.</p>
        <p>Walking along the Moika Embankment expect to see architecture of XVIII-XX centuries, and such sights as St. Michael's Castle, Summer Garden, Mikhailovsky Garden, Mikhailovsky Palace, Field of Mars, House of Adamini, Round Market, Stables yard, Pushkin Museum, Capella, General Staff Building, Stroganov Palace, the Razumovsky  Palace, the Mariinsky Palace, the building of the Ministry of State Property, Yusupov Palace, the Palace of the Grand Duchess Xenia Alexandrovna, New Holland , Central Naval Museum, Palace of Grand Duke Alexei Alexandrovich.</p>
    </li>
    <li>
        <h6>Blue, Green and Pevchesky Bridges</h6>
        <p>Be ready to turn blue, green and sing!</p>
        <p>Overall 15 bridges cross the Moika river. We find these ones fun:</p>
        <p>Singing (Pevchesky) Brdige</p>
        <p>Kissing (Potseluev) Bridge</p>
        <p>Green Bridge</p>
        <p>Red Bridge</p>
        <p>Blue Bridge. By the way, Blue bridge is located on St. Isaac's Square, it is the widest bridge in the city (100 m wide), and is considered an invisible bridge, as it's built in the square so harmonically. Let's find it together!</p>
    </li>
    <li>
        <h6>Museum of Soviet Arcade Machines</h6>
        <p>This one is quite a change from the all the touristy sights, yet historical as well, in its own way.</p>
        <p>Perfect atmosphere of some secret bunker of arcade games and fun,  that you won't experience anywhere else.</p>
        <p>For your entrance fee you'll get tokens to play the games from the Soviet Era.</p>
        <p>The soda machine which works only on Soviet Era coins and souvenirs like cute toy replicas of robots from the past will add up to the atmosphere.</p>
        <p>Refreshed and happy like kids we move on.</p>
    </li>
    <li>
        <h6>Church on Spilled Blood</h6>
        <p>7000 sq m of mosaics and 400,000 pieces of artwork dating back to the 10th century are not the only features of the most colorful churches of Saint Petersburg.</p>
        <p>The church is very symbolic with a tragic past as it was built at the place where Tsar Alexander II was mortally wounded on March 1, 1881. The height of the temple is 81 meters, which symbolizes the year of the death of the Tsar.</p>
        <p>Among other elements of luxury decoration are elegant cathedral chapter, huge multicolored domes, complex mosaics and carvings. This original masterpiece of ancient architecture is a strong reminiscent of Moscow's St. Basil's Cathedral.</p>
        <p>After all the historical and cultural charm we will take a walk to the Mikhailovsky Garden and Field of Mars.</p>
    </li>
    <li>
        <h6>Michael Garden and Palace</h6>
        <p>One of the most beautiful parks and symbols of St Petersburg. Michael Garden is especially good if you have no time or desire to explore the islands and villages in St Petersburg region, like Pushkin, Pavlovsk and others.</p>
        <p>Lovely French and English-style park designs, stunning landscape, 279-year-old oak tree, and the history of the place make this Garden very special.</p>
        <p>The history of the area began in Peter's time in XVIII century. Here was built a small palace for Peter I's wife, Catherine  (on the site of present-pier pavilion Rossi). This palace, known as the "Golden mansion", was surrounded by a garden, consisting of fruit trees. The garden had 5 ponds with live fish and greenhouses for growing exotic fruits. Several dozens of nightingales from the cities of Novgorod and Pskov were brought to Michael Garden specially for royal family.</p>
        <p>Thematic landscape and garden annual festivals attracted the attention of the members of the British royal family, related to the Russian Imperial House of Romanov - Prince Michael of Kent and his wife, who took part in the festival in 2014.</p>
        <p>Mikhailovsky (Michael) Garden is included in the Unified State Register of cultural heritage of Russia.</p>
    </li>
    <li>
        <h6>Statue of Chizhik Pyzhik</h6>
        <p>Chizhik-Pyzhik, where have you been?
        <p>Drank vodka on the Fontanka.
        <p>Took a shot, took another -
        <p>Got dizzy.
        <p>This national rhyme was famous in 1990-s and is still popular among Russians, symbolizing college years and sweet carelessness of students.</p>
        <p>The statue of Chizhik Pyzhik is the smallest in St Petersburg, with the height of only 11 centimeters.</p>
        <p>The first monument was installed 21 years ago, in 1994, and has been stolen at least 3 times since then.</p>
        <p>The inspiration for the statue dates back to XIX century, and you'll hear about the story of Chizhi Pyzhik on the tour.</p>
    </li>
    <li>
        <h6>Field of Mars</h6>
        <p>Yes, the park was named after Mars, the Roman god of war ((Like the Field of Mars in ancient Rome and Paris). and has always been war-thematic, tracking the war moments of St Petersburg throughout all the history of the city.</p>
        <p>The vast area used to be the place for military parades in XVIII century. Emperor Paul I, distinguished by a special love for the military parades on the Field of Mars, even ordered to build his official residence near the park - the Mikhailovsky Castle.</p>
        <p>During the siege of Leningrad Field of Mars was used as a vegetable garden where citizens grew vegetables. And on January 27, 1944 there was a great a salute in honor of lifting the blockade of Leningrad.</p>
        <p>The eternal flame has been burning since 1957 and was the first eternal flame in Russia.</p>
        <p>Field of Mars is peculiar for its memorial architecture of the early XX century, and is surrounded by various attractions, like architectural ensemble of Trinity Bridge on Suvorov Square, a monument to Alexander Suvorov, marble palace, Swan Canal Quay, Moika embankment, Summer Garden, a monument to Alexander III, barracks of Pavlovsky Regiment, Michael Castle, St. Michael's Garden, Church of the Savior on Blood.</p>
    </li>
    <li>
        <h6>Monuments to Catherine the Great and Paul I</h6>
        <p>Amazing stories of Russian political leaders: national heroes with their own life stories, love intrigues, lessons they learnt and actions that led to a brand new Russia. We'll talk about it on the tour.</p>
    </li>
</ul>
